def sum(a: int, b: int) -> int:
    '''a + b'''
    return a + b

def mul(a: int, b: int) -> int:
    '''a * b'''
    return a * b

def minus(a: int, b: int) -> int:
    '''a - n'''
    return a - b

def pow(a: int, b: int) -> int:
    '''a**b'''
    return a**b

def divide(a: int, b: int) -> int:
    '''a / b'''
    return a // b
